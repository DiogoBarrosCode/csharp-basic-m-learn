/*

Operadores compostos.

Operadores como +=, -=, *=, /=, compõem uma operação além de atribuiro resultado à variável.
O operador += é chamado especificamente de operador de atribuição de adição.

As duas operações a seguir são equivalentes.

*/

int value1 = 0;
int value2 = 0;

value1 = value1 + 5;
value2 += 5;

Console.WriteLine(value1);
Console.WriteLine(value2);