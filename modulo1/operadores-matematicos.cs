/*

Operadores Matemáticos.

1 + é o operador de adição.
2 - é o operador de subtração.
3 * é o operador de multiplicação.
4 / é o operador de divisão.

*/

int soma = 7 + 5;
int subtracao = 7 - 5;
int multiplicacao = 7 * 5;
decimal divisao = 7.0m / 5;

Console.WriteLine("Soma: " + soma);
Console.WriteLine("Subtração: " + subtracao);
Console.WriteLine("Multiplicacao: " + multiplicacao);
Console.WriteLine("Divisao: " + divisao);

/*

O operador de resto % informa o resto da divisão int.
O que você realmente aprende com isso é se um número é divisível por outro.

*/

int resto1 = 7 % 5;
int resto2 = 200 % 5;
Console.WriteLine("Resto de divisão: " + resto1);
Console.WriteLine("Resto de divisão: " + resto2);