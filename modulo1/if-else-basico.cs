/*

Lógica de decisão.

A instrução if usa uma expressão booliana colocada entre parênteses.
Se a expressão for verdadeira, o código após a instrução if será executado.
Caso contrário, o runtime do .NET ignorará o código passará para a instrução else.

*/

int num = 10;

if (num > 9)
{
    Console.WriteLine("Verdadeiro!");
}
else
{
    Console.WriteLine("Falso!");
}